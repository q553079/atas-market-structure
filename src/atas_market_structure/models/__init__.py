from pydantic import BaseModel


_MODEL_REBUILD_NAMESPACE = {**globals(), "Any": Any}
for _value in tuple(globals().values()):
    if not isinstance(_value, type):
        continue
    if not issubclass(_value, BaseModel):
        continue
    try:
        _value.model_rebuild(_types_namespace=_MODEL_REBUILD_NAMESPACE, raise_errors=False)
    except AttributeError:
        continue
